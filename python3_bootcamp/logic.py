# conditional logic to make decisions!
# if elif else
name = input("What is your name?: ")
if name == "Arya Stark":
  print("Valar Morghulis")
elif name == "Jon Snow":
  print("You know nothing")
elif name == "Daenerys Targaryen":
  print("The Unburnt, The Breaker of Chains, The Mother of Dragons")
else:
  print("Carry on!")

